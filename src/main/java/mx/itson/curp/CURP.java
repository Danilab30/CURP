    public CURP() {
        //STEP 1: AQUI ESTAMOS HACIENDO EL PRIMER PASO, PEDIR LA INFORMACION AL USUARIO CONVIRTIENDOLO EN MAYUSCULAS
        String primerApellido = txtApellidoP.getText().toUpperCase();
       
        String segundoApellido = txtApellidoM.getText().toUpperCase();
      
        String nombre = txtNombre.getText().toUpperCase();
        
        String fechaNacimiento = txtFecha.getText();
      
        char sexo = txtHM.getText().charAt(0);
      
        String entidadNacimiento = txtEntidad.getText().toUpperCase();
        
        
        // STEP 2 SE EMPIEZA A CREAR ESTRUCTURA PARA LOS METODOS
        char primeraVocal = obtenerPrimeraVocal(primerApellido);
        
        
        // AQUI OBTENEMOS LA PRIMERA LETRA DEL PRIMER APELLIDO CON EL CHART
        char primeraLetraSegundoApellido = segundoApellido.charAt(0);

        // OBTENEMOS PRIMERA LETRA DE MI NOMBRE 
        char primeraLetraNombre = obtenerPrimeraLetraNombre (nombre);

        // QUITAREMOS LOS GUINES QUE EL USUARIO LE AGREGUE EN EL TXT, PEDIMOS LA FECHA 
        String fechaNacimientoFormateada = fechaNacimiento.replaceAll("-", "");
        // AQUI AGARRAMOS SOLAMENTE LOS ULTIMOS 6 DIGITOS DE LA FECHA, COMO? CON EL LENGHT ESTE NOS DA LA CANTIDAD DE NUMEROS QUE HAY Y EL 
        //SUBSTRING DE LA CADENA DE LA FECHA NOS DA LA MAS PEQUEÃ‘A QUE NECESITAMOS QUE VENDRIAN SIENDO LOS ULTIMOS 6
        String ultimos6Caracteres = fechaNacimientoFormateada.substring(fechaNacimientoFormateada.length() - 6);


        char primeraConsonantePrimerApellido = obtenerPrimeraConsonanteInterna(primerApellido);

        
        char primeraConsonanteSegundoApellido = obtenerPrimeraConsonanteInterna(segundoApellido);

      
        char primeraConsonanteNombre = obtenerPrimeraConsonanteInterna(nombre);

     
        char digitoFechaNacimiento = obtenerDigitoFechaNacimiento(fechaNacimiento);
        

        String curp = "" + primerApellido.charAt(0) + primeraVocal + primeraLetraSegundoApellido
                + primeraLetraNombre + ultimos6Caracteres + sexo + entidadNacimiento
                + primeraConsonantePrimerApellido + primeraConsonanteSegundoApellido
                + primeraConsonanteNombre + digitoFechaNacimiento + "1";

        JOptionPane.showMessageDialog(this, curp, "CURP",HEIGHT);
    }
