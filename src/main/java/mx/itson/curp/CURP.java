    private char obtenerPrimeraVocal(String cadena) {
        for (int i = 1; i < cadena.length(); i++) {
            char c = cadena.charAt(i);
            if (c == 'A' || c == 'E' || c == 'I' || c == 'O' || c == 'U') {
                return c;
            }
            
        }
        return 'X';// Si no se encuentra ninguna consonante interna, se coloca 'X'
    }
